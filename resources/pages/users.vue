<template>
  <v-layout column justify-center align-end>
    <v-btn @click="createNewUser()" color="primary" dark class="mb-2">Nový uživatel</v-btn>
    <v-dialog v-model="dialog" max-width="500px">
      <v-card>
        <v-card-title>
          <span class="headline">{{ modalTitle }}</span>
        </v-card-title>

        <v-form ref="form">
          <v-card-text>
            <v-container grid-list-md>
              <v-layout wrap column>
                <v-flex xs12 sm6 md4>
                  <v-text-field :rules="[rules.required]" v-model="modalItem.username"
                                label="Přihlašovací jméno"></v-text-field>
                </v-flex>
                <v-flex xs12 sm6 md4>
                  <v-text-field type="password" v-model="modalItem.password"
                                label="Heslo"></v-text-field>
                </v-flex>
                <v-flex xs12 sm6 md4>
                  <v-text-field :rules="[rules.required]" v-model="modalItem.firstName" label="Jméno"></v-text-field>
                </v-flex>
                <v-flex xs12 sm6 md4>
                  <v-text-field :rules="[rules.required]" v-model="modalItem.lastName" label="Příjmení"></v-text-field>
                </v-flex>
                <v-flex xs12 sm6 md4>
                  <v-text-field type="number" v-model="modalItem.totalExp"
                                label="Expy"></v-text-field>
                </v-flex>
                <v-flex xs12 sm6 md4>
                  <v-select
                          label="Role"
                          :items="roleItems"
                          multiple
                          v-model="modalItem.roles">
                  </v-select>
                </v-flex>
                <v-flex xs12 sm6 md4>
                  <v-checkbox
                    label="Aktivní"
                    v-model="modalItem.isActive"
                  ></v-checkbox>
                </v-flex>
              </v-layout>
            </v-container>
          </v-card-text>

          <v-card-actions>
            <v-spacer></v-spacer>
            <v-btn color="blue darken-1" flat @click.native="close">Zrušit</v-btn>
            <v-btn color="blue darken-1" flat @click.native="save">Uložit</v-btn>
          </v-card-actions>
        </v-form>
      </v-card>
    </v-dialog>

    <v-data-table
      :headers='headers'
      :items='users'
      item-key="id"
      hide-actions
      fill-height
      must-sort
      class='elevation-1 fullscreen'
    >
      <template slot='items' slot-scope='props'>
        <td class='text-xs-center element'>{{ `${props.item.firstName} ${props.item.lastName}` }}</td>
        <td class='text-xs-center element'>{{ props.item.username }}</td>
        <td class='text-xs-center element'>
          <span v-for="(value, index) in props.item.roles">
            <span>{{value}}</span><span v-if="index+1 < props.item.roles.length">, </span>
          </span>
        </td>
        <td class='text-xs-center element'>{{ props.item.level }}</td>
        <td class='text-xs-center element'>{{ props.item.totalExp }}</td>
        <td class='text-xs-center element'>{{ props.item.isActive? 'ano' : 'ne' }}</td>
        <td class="justify-center layout px-0">
          <v-icon
            small
            class="mr-2"
            @click="editItem(props.item)"
          >
            edit
          </v-icon>
          <v-icon
            small
            @click="deleteItem(props.item)"
          >
            delete
          </v-icon>
        </td>

      </template>
    </v-data-table>
  </v-layout>
</template>

<script>
import { mapState } from 'vuex';

export default {
  async fetch ({ store, params }) {
    await Promise.all([
      store.dispatch('getUsers'),
      store.dispatch('getRoles'),
    ]);
  },
  computed: {
    ...mapState([
      'users',
      'roles',
    ]),
    headers: function () {
      return [
        {
          text: 'Jméno',
          align: 'center',
          sortable: true,
          value: 'firstName',
        },
        {
          text: 'Přihlašovací jméno',
          align: 'center',
          sortable: true,
          value: 'username',
        },
        {
          text: 'Role',
          align: 'center',
          sortable: true,
          value: 'roles',
        },
        {
          text: 'Level',
          align: 'center',
          sortable: true,
          value: 'level',
        },
        {
          text: 'Expy',
          align: 'center',
          sortable: true,
          value: 'totalExp',
        },
        {
          text: 'Aktivní',
          align: 'center',
          sortable: true,
          value: 'isActive',
        },
        {
          text: 'Akce',
          align: 'center',
          sortable: false,
          value: 'actions',
        },
      ];
    },
    roleItems () {
      return this.roles.map(r => ({
        text: r.name,
        value: r.slug,
      }));
    },
  },
  data () {
    return {
      dialog: false,
      modalTitle: '',
      rules: {
        required: value => !!value || 'Povinné.',
      },
      modalItem: {
        id: null,
        firstName: '',
        lastName: '',
        password: '',
        totalExp: 0,
        isActive: true,
        username: '',
        roles: ['user'],
      },
      defaultModalItem: {
        id: null,
        firstName: '',
        lastName: '',
        password: '',
        totalExp: 0,
        isActive: true,
        username: '',
        roles: ['user'],
      },
    };
  },
  methods: {
    createNewUser () {
      this.modalItem = { ...this.defaultModalItem };
      this.modalTitle = 'Nový uživatel';
      this.dialog = true;
    },
    editItem (item) {
      this.modalItem = {
        id: item.id,
        firstName: item.firstName,
        lastName: item.lastName,
        totalExp: item.totalExp,
        isActive: item.isActive === 1,
        username: item.username,
        roles: item.roles,
      };

      this.modalTitle = 'Upravit uživatele';
      this.dialog = true;
    },
    async deleteItem (item) {
      const confirmed = confirm(`Opravdu chcete smazat uživatele ${item.firstName} ${item.lastName}?`);

      if (confirmed) {
        await this.$store.dispatch('deleteUser', item.id);
        await this.$store.dispatch('getUsers');
      }
    },
    close () {
      this.dialog = false;
      this.modalItem = { ...this.defaultModalItem };
    },
    async save () {
      if (this.modalItem.id) {
        await this.$store.dispatch('editUser', this.modalItem);
      } else {
        await this.$store.dispatch('createUser', this.modalItem);
      }

      this.dialog = false;
    },
  },
};
</script>

